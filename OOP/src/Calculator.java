
public class Calculator {
	public void getAddition(double x, double y) {
		System.out.println(x +" + " + y +" = "+ (x + y));
	}
	public void getSubtraction(double x, double y) {
		System.out.println(x +" - "+ y +" = "+ (x - y));
	}
	public void getDivision(double x, double y) {
		System.out.println(x +" / "+ y +" = "+ (x / y));
	}
	public void getMultiplication(double x, double y) {
		System.out.println(x +" * "+ y +" = "+ (x * y));
	}

}
